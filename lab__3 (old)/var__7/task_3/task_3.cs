using System;

class Program
{
    static void Main()
    {
        // Запрос у пользователя ввода масти и достоинства карты
        Console.WriteLine("Введите номер масти (1 - пики, 2 - трефы, 3 - бубны, 4 - червы):");
        int suitNumber = Convert.ToInt32(Console.ReadLine());

        Console.WriteLine("Введите номер достоинства карты (6 - 14):");
        int rankNumber = Convert.ToInt32(Console.ReadLine());

        // Определение масти карты
        string suit = suitNumber switch
        {
            1 => "пик",
            2 => "треф",
            3 => "бубен",
            4 => "червей",
            _ => "неизвестной масти" // на случай, если введённое число не входит в диапазон
        };

        // Определение достоинства карты
        string rank = rankNumber switch
        {
            6 => "Шестерка",
            7 => "Семерка",
            8 => "Восьмерка",
            9 => "Девятка",
            10 => "Десятка",
            11 => "Валет",
            12 => "Дама",
            13 => "Король",
            14 => "Туз",
            _ => "неизвестного достоинства" // на случай, если введённое число не входит в диапазон
        };

        // Вывод полного названия карты
        if (suit == "неизвестной масти" || rank == "неизвестного достоинства")
        {
            Console.WriteLine("Введены некорректные номера масти или достоинства карты.");
        }
        else
        {
            Console.WriteLine($"Полное название карты: {rank} {suit}");
        }

        // Добавляем ожидание нажатия клавиши перед завершением
        Console.WriteLine("Нажмите любую клавишу для завершения...");
        Console.ReadKey();
    }
}
