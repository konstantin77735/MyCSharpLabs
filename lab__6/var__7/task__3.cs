using System;

class Program
{
    static void Main()
    {
        // Левая граница интервала
        int number = 500;
        // Счётчик найденных чисел
        int count = 0;

        // Цикл выполняется, пока не найдём 20 чисел
        while (count < 20)
        {
            // Проверяем делимость на 13 и отсутствие делимости на 17
            if (number % 13 == 0 && number % 17 != 0)
            {
                // Выводим число
                Console.WriteLine(number);
                // Увеличиваем счётчик найденных чисел
                count++;
            }

            // Переход к следующему числу
            number++;
        }

        // Завершаем программу
        Console.WriteLine("Поиск завершён. Найдено 20 чисел.");
    }
}
